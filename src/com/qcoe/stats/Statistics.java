package com.qcoe.stats;

/**
 *
 * @author kbose
 */
public class Statistics {
    
    private String statisticsDetailKey;
    private String statisticsName;
    private double statisticsValue;

    public String getStatisticsDetailKey() {
        return statisticsDetailKey;
    }

    public void setStatisticsDetailKey(String statisticsDetailKey) {
        this.statisticsDetailKey = statisticsDetailKey;
    }

    
    
    public String getStatisticsName() {
        return statisticsName;
    }

    public void setStatisticsName(String statisticsName) {
        this.statisticsName = statisticsName;
    }

    public double getStatisticsValue() {
        return statisticsValue;
    }

    public void setStatisticsValue(double statisticsValue) {
        this.statisticsValue = statisticsValue;
    }
    
    
    
}
